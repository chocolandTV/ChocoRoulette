// SongInfo
public class SongInfo
{
    public string skinName;

    public string artist;

    public string year;

    public string mode;

    public string game;

    public string img;

    public SongInfo(string SkinName, string Artist, string Year, string Mode, string Game, string Img)
    {
        skinName = SkinName;
        artist = Artist;
        year = Year;
        mode = Mode;
        game = Game;
        img = Img;
    }
}
